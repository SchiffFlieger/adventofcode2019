package days

import (
	"fmt"
	"math"
	"strings"
)

func Day8Part1() int {
	type count struct {
		zero, one, two int
	}

	input := day8input()
	w, h := day8ImageSize()
	layers := len(input) / (w * h)

	counts := make([]count, layers)
	for l := 0; l < layers; l++ {
		c := count{}
		for y := 0; y < h; y++ {
			for x := 0; x < w; x++ {
				switch input[l*w*h+w*y+x] {
				case 0:
					c.zero++
				case 1:
					c.one++
				case 2:
					c.two++
				}
			}
		}
		counts[l] = c
	}

	result, min := 0, math.MaxInt64
	for i := 0; i < layers; i++ {
		if counts[i].zero < min {
			min = counts[i].zero
			result = counts[i].one * counts[i].two
		}
	}

	return result
}

func Day8Part2() string {
	input := day8input()
	w, h := day8ImageSize()
	layers := len(input) / (w * h)

	finalImage := make([]int, w*h)
	for i := 0; i < len(finalImage); i++ {
		finalImage[i] = 2 // fill with transparent pixels
	}

	for l := 0; l < layers; l++ {
		for y := 0; y < h; y++ {
			for x := 0; x < w; x++ {
				pos := y*w + x
				if finalImage[pos] == 2 {
					finalImage[pos] = input[l*w*h+pos]
				}
			}
		}
	}

	return strings.Trim(strings.Join(strings.Fields(fmt.Sprint(finalImage)), ""), "[]")
}

